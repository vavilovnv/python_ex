import inspect
from functools import wraps

def recviz(f):
    @wraps(f)
    def wrapper(*args, **kwargs):
        a = list(map(repr, args))
        b = [f'{k}={v!r}' for k, v in kwargs.items()]
        params = ', '.join(a + b)
        c = '    ' * (len(inspect.getouterframes(inspect.currentframe())) // 2 - 1)
        print(c + f'-> {f.__name__}({params})')
        res = f(*args, **kwargs)
        print(c + f'<- {repr(res)}')
        return res
    return wrapper
