class Matrix:
    def __init__(self, rows, cols, value=0):
        self.rows = rows
        self.cols = cols
        self._matrix = [[value for _ in range(cols)] for _ in range(rows)]

    def get_value(self, row, col):
        return self._matrix[row][col]
    
    def set_value(self, row, col, value):
        self._matrix[row][col] = value
    
    def __repr__(self):
        return f"Matrix({self.rows}, {self.cols})"
    
    def __str__(self):
        return "\n".join([" ".join(map(str, row)) for row in self._matrix])
    
    def __pos__(self):
        mx = Matrix(self.rows, self.cols)
        mx._matrix = self._matrix[::]
        return mx
    
    def __neg__(self):
        mx = Matrix(self.rows, self.cols)
        mx._matrix = [
            [-self._matrix[i][j] for j in range(self.cols)]
            for i in range(self.rows)
        ]
        return mx
        
    def __invert__(self):
        mx = Matrix(self.cols, self.rows)
        mx._matrix = [
            [self._matrix[j][i] for j in range(self.rows)]
            for i in range(self.cols)
            ]
        return mx
    
    def __round__(self, value=None):
        mx = Matrix(self.rows, self.cols)
        mx._matrix = self._matrix[::]
        if value:
            mx._matrix = [
                [round(mx._matrix[i][j], value) for j in range(mx.cols)]
                for i in range(mx.rows)
                ]
        return mx
     