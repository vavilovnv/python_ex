class WriteSpy:
    def __init__(self, file1, file2, to_close=False):
        self.file1 = file1
        self.file2 = file2
        self.to_close = to_close

    def __enter__(self, *args):
        return self

    def __exit__(self, exc_type, exc_value, traceback):
        if self.to_close:
            self.file1.close()
            self.file2.close()
        return False

    def write(self, text):
        if self.file1.closed or self.file2.closed or not self.writable():
            raise ValueError("–§–∞–π–ª –∑–∞–∫—Ä—ã—Ç –∏–ª–∏ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω –¥–ª—è –∑–∞–ø–∏—Å–∏")
        self.file1.write(text)
        self.file2.write(text)

    def close(self):
        self.file1.close()
        self.file2.close()

    def closed(self):
        return self.file1.closed and self.file2.closed

    def writable(self):
        if not (self.file1.closed or self.file2.closed):
            return self.file1.writable() and self.file2.writable()
        return False
