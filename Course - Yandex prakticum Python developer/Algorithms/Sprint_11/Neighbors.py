"""
Соседи.

Дана матрица. Нужно написать функцию, которая для элемента возвращает всех его
соседей. Соседним считается элемент, находящийся от текущего на одну ячейку
влево, вправо, вверх или вниз. Диагональные элементы соседними не считаются.

Формат ввода
В первой строке задано n — количество строк матрицы. 
Во второй — количество столбцов m. Числа m и n не превосходят 1000. 
В следующих n строках задана матрица. Элементы матрицы — целые числа, по
модулю не превосходящие 1000. 
В последних двух строках записаны координаты элемента, соседей которого нужно
найти. Индексация начинается с нуля.

Формат вывода
Напечатайте нужные числа в возрастающем порядке через пробел.

Пример 1
Ввод
4
3
1 2 3
0 2 6
7 4 1
2 7 0
3
0
Вывод
7 7

Пример 2
Ввод
4
3
1 2 3
0 2 6
7 4 1
2 7 0
0
0
Вывод
0 2
"""

n, m = int(input()), int(input())
mtx = [[int(i) for i in input().split()] for _ in range(n)]
x, y = int(input()), int(input())
result = []
if x - 1 >= 0:
    result.append(mtx[x - 1][y])
if x + 1 <= n - 1:
    result.append(mtx[x + 1][y])
if y - 1 >= 0:
    result.append(mtx[x][y - 1])
if y + 1 <= m - 1:
    result.append(mtx[x][y + 1])
print(*sorted(result))
